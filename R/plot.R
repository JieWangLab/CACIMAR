  if(Show.Module==T){

    if(is.null(ModuleColor1)){
      ModuleColor1 <- c(rgb(255/255,255/255,255/255), rgb(152/255,152/255,152/255), rgb(72/255,85/255,167/255))
    }else{
      if(!is.null(RowGroup1) & is.factor(RowGroup1)){ ModuleColor1 <- ModuleColor1[c(1,NameInd1+1)]; }
    }

    if(K1==1){
      ph1 <- pheatmap::pheatmap(RNA3[, 1], clustering_distance_rows=clustering_distance_rows, show_rownames=F, show_colnames=show_colnames, border_color='white', cluster_rows=F, cluster_cols=cluster_cols, color = ModuleColor1, legend=F, silent=T, breaks=seq(1, nrow(RNA3)))
    }else{
      ph1 <- pheatmap::pheatmap(RNA3[, 1], clustering_distance_rows=clustering_distance_rows, show_rownames=F, show_colnames=show_colnames, border_color='white', cluster_rows=F, cluster_cols=cluster_cols, color = ModuleColor1, legend=F, silent=T)
    }
    if(ByPanel==T){
      uColumnGroup1 <- unique(ColumnGroup1)
      RNA31 <- RNA3[, 2:(length(ColumnGroup1[ColumnGroup1==uColumnGroup1[1]])+1)]
      ph12 <- pheatmap::pheatmap(RNA3[, 2:(length(ColumnGroup1[ColumnGroup1==uColumnGroup1[1]])+1)], clustering_distance_rows=clustering_distance_rows, clustering_method=clustering_method, show_rownames=show_rownames, show_colnames=show_colnames, labels_row=Gene2, border_color=border_color, cluster_rows=F, cluster_cols=cluster_cols, color = colorRampPalette(Color1)(100), fontsize=fontsize, legend=legend1, silent=T)
      ph2 <- pheatmap::pheatmap(RNA31, clustering_distance_rows=clustering_distance_rows, clustering_method=clustering_method, show_rownames=show_rownames, show_colnames=show_colnames, labels_row=Gene2, border_color=border_color, cluster_rows=F, cluster_cols=cluster_cols, color = colorRampPalette(Color1)(100), fontsize=fontsize, legend=legend1, silent=T)
