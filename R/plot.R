          GroupLeng1 <- length(ColumnGroup1[ColumnGroup1<uColumnGroup1[i]])
          GroupLeng2 <- length(ColumnGroup1[ColumnGroup1==uColumnGroup1[i]])
          RNA32 <- RNA3[, (GroupLeng1+NumColumnBlank1*(i-1)+2):(GroupLeng1+NumColumnBlank1*(i-1)+GroupLeng2+1)]
          ph3 <- pheatmap::pheatmap(RNA32, clustering_distance_rows=clustering_distance_rows, clustering_method=clustering_method, show_rownames=show_rownames, show_colnames=show_colnames, labels_row=Gene2, border_color=border_color, cluster_rows=F, cluster_cols=cluster_cols, color = colorRampPalette(Color1)(100), fontsize=fontsize, legend=legend1, silent=T)
